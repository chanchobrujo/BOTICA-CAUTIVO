    private void renderGraphics(String top){   
        jLabel1.setIcon(null);
        DefaultPieDataset pieDataset = new DefaultKeyedValuesDataset();
        String date_1 = Commons.dateValue(jDateChooser1.getDate());
        String date_2 = Commons.dateValue(jDateChooser2.getDate());
        
        this.moduleReports.productsTop(top, date_1, date_2).forEach(action -> {
            pieDataset.setValue(action.getName(), action.getCount());
        });
        
        JFreeChart jFreeChart = ChartFactory.createPieChart3D("Productos m√°s vendidos", pieDataset, true, true, true);
        
        BufferedImage graficotorta = jFreeChart.createBufferedImage(710, 430);
        jLabel1.setIcon(new ImageIcon(graficotorta)); 
    }

